var compOps = {
    '>=': function(a, b) { return (a) > b }, // not a typo! compares top-left corner of each box
    // example: if a = 0 and is the upper y-bound of a 1x1 box and the STL criteria is >= 0, this box goes from -1 to 0 and should NOT be colored in
    '>': function(a, b) { return (a) > b },
    '<=': function(a, b) { return a <= b },
    '<': function(a, b) { return a < b },
};

function colorBoxes(nWide, nTall, cnvsSize, gridGroup, jsonSTL) {
    /* uses the STL formula to determine how system behavior is expected to be specified
    and colors the boxes accordingly */
    grid.activate(); // Define active layer:
    stlBounds = $.parseJSON(jsonSTL);

    for (i = 0; i < gridGroup.children.length; i++) {
        var firstCorner = changeCoordsToSTL(gridGroup.children[i].segments[1]); // targets top-left corner
        for (var j = 0; j < stlBounds.length; j++) {
            var leftBound = stlBounds[j].G.substring(1,4);
            var rightBound = stlBounds[j].G.substring(6,9);
              // if the segment falls within the x-axis (temporal) bounds:
              if (firstCorner.x >= leftBound && firstCorner.x < rightBound) {
                gridGroup.children[i].fillColor = null; // each time a new part of the STL formula that applies to the box is compared, reset the color                
                // compare if >= or <= the defined y-axis (spatial) bound
                var compOp = stlBounds[j].x.substring(0,2);
                if (compOps[stlBounds[j].x.substring(0,2)](firstCorner.y, stlBounds[j].x.substring(3,7))) {
                    gridGroup.children[i].fillColor = "#0275d8";
                } else {
                    break; // move on to next box
                }
            } 
        }
    }
    cnvs.activate(); // Define active layer
}

