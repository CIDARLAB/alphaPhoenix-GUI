// checks if a line falls into the correct zone
function checkOutliers(path) { 
    // checks a given path for outliers, color red if yes
    stlBounds = $.parseJSON(jsonSTL); // parses JSON file with STL formula
    // for each segment on the path
    for (var i = 0; i < path.segments.length; i++) {

        var currentSeg = changeCoordsToSTL(path.segments[i]) // convert canvas coords to STL values
        var OOB = true; // assume the segment is out of bounds of the x-axis bounds to begin
        for (var j = 0; j < stlBounds.length; j++) { // iterate through each bound
            var leftBound = stlBounds[j].G.substring(1,4); 
            var rightBound = stlBounds[j].G.substring(6,9);

            // if the segment falls within the x-axis (temporal) bounds:
            if (currentSeg.x >= leftBound && currentSeg.x <= rightBound) {
                OOB = false;
                // compare if >= or <= the defined y-axis (spatial) bound
                if (stlBounds[j].x.substring(0,2) == '>=') {
                    if (currentSeg.y < stlBounds[j].x.substring(3,7)) {
                        path.strokeColor = 'red';
                    }
                } else {
                    if (currentSeg.y > stlBounds[j].x.substring(3,7)) {
                        path.strokeColor = 'red';
                    }
                }
            } 
        }
        if (OOB == true) {
            // if the segment never fits into the x-axis bounds described in the STL, color it red
            path.StrokeColor = 'red';
        }
    }
}

